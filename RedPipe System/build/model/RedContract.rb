class RedContract

    #=======================================================#
    def fromFile(arquivo)
        @data = Hash.new

        File.open(arquivo, "r") do |infile|
            while (line = infile.gets)
                key, value = line.split(":")
                @data[key.to_sym] = value.chop
            end
        end
    end
    #=======================================================#

    #=======================================================#
    def getClassFile
        return @data[:file]
    end
    #=======================================================#

    #=======================================================#
    def getCommand
        return @data[:command]
    end
    #=======================================================#

    #=======================================================#
    def getDescription
        return @data[:description]
    end
    #=======================================================#

end
